package org.pgcl.enums;

public enum  Area {

	SIRAJGANJ(0,"Sirajganj"),
	BAGHABARI(1,"BAGHABARI"),
	PABNA(2,"PABNA"),
	ISHWARDI(3,"ISHWARDI"),
	BOGRA(4,"BOGURA"),
	Shahjadpur(5,"Shahjadpur"),
	BERA(6,"BERA"),
	SHANTHIA(7,"SHANTHIA"),
	ULLAHPARA(8,"ULLAHPARA"),
	RAJSHAHI(9,"RAJSHAHI"),
	NOVEMBER(10,"November"),
	DECEMBER(11,"December");

    private String label;
    private int id;

    private Area(int id,String label) {
        this.id=id;
    	this.label = label;
    }

    public String getLabel() {
        return label;
    }
    public int getId() {
        return id+1;
    }

    
}
